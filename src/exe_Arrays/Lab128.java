package exe_Arrays;

public class Lab128 {
    public static void main(String[] args) {
        int[] a = {27, 31, 32, 33};
        System.out.println(a.length);

        //How to print all elements in an array?
        //Loop
        //for loop
        for (int i = 0; i < a.length ; i++) { //0,1,2,3
            System.out.println(a[i]);
        }
    }
}
